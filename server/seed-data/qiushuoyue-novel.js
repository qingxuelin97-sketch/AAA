// 注入到 better-sqlite3 数据库（幂等：同名作品会先删后建）。返回新建 novel id。
export function seedQiushuoyueNovel(db, ownerId) {
  const def = QIUSHUOYUE_NOVEL;
  const ex = db.prepare('SELECT id FROM novels WHERE owner_id = ? AND title = ?').get(ownerId, def.title);
  if (ex) db.prepare('DELETE FROM novels WHERE id = ?').run(ex.id); // 级联删除 runs/beats
  let seq = 0;
  const codex = (def.codex || []).map((e, i) => ({
    id: 'q' + (Date.now().toString(36)) + (seq++) + i.toString(36),
    title: e.title || '未命名设定', category: e.category || 'other', trigger: e.trigger || 'keyword',
    keys: e.keys || '', content: e.content || '', source: 'meta', locked: 0, enabled: 1,
    updated_at: new Date().toISOString(),
  }));
  const info = db.prepare(`INSERT INTO novels (owner_id, title, logline, synopsis, genre, tags, style, codex, pinned)
    VALUES (?,?,?,?,?,?,?,?,1)`).run(
    ownerId, def.title, def.logline || '', def.synopsis || '', def.genre || '', def.tags || '',
    JSON.stringify(def.style || {}), JSON.stringify(codex));
  const novelId = info.lastInsertRowid;
  // 复刻局外母版为局内设定，开「主线」
  const canon = codex.map(e => ({ ...e, source: 'meta', updated_at: new Date().toISOString() }));
  db.prepare('INSERT INTO novel_runs (novel_id, owner_id, name, canon) VALUES (?,?,?,?)')
    .run(novelId, ownerId, '主线', JSON.stringify(canon));
  return novelId;
}
